func TestMainOutput(t *testing.T) {
	var buf bytes.Buffer
	out = &buf

	main()

	expected := "1\n1\n2\n3\n5\n8\n13\n"
	actual := buf.String()

	if expected != actual {
		t.Errorf("Unexpected output in main()")
	}
}

